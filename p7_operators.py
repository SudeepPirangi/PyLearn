print("\nLogical Operators - and, or, not\n")
print("1 == 1 and 1 == 2 -", 1 == 1 and 1 == 2)

print("1 == 1 or 1 == 2 -", 1 == 1 or 1 == 2)
print("1 == 2 or 2 == 2 -", 1 == 2 or 2 == 2)
print("1 == 2 or 2 == 1 -", 1 == 2 or 2 == 1)

print("10 > 5 -", 10 > 5)
print("not(10 > 5) -", not(10 > 5))

print("\nConditional Operators\n")
print("12 == 23 -", 12 == 23)
print("12 == '12' -", 12 == '12')
print("'12' == 12 -", '12' == 12)

print("12 != 23 -", 12 != 23)
print("12 != '12' -", 12 != '12')

print("10 > 20 -", 10 > 20)
print("10 >= 10 -", 10 >= 10)
print("10 < 20 -", 10 < 20)
print("10 <= 10 -", 10 <= 10)

print("\nArithmetic Operators\n")
print("10 ** 2 -", 10 ** 2)
print("100 ** 0.5 -", 100 ** 0.5)